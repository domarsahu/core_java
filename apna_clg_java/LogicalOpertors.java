class LogicalOpertors{

public static void main(String []args)

{

// AND operator 

System.out.println(10>20 &&20<10);
System.out.println(10<20 &&'a'<'b');
// OR operator 
System.out.println(10>20 || 20>10);
System.out.println(10>20 || 'a'>'b');


//NOT operator 

System.out.println(!(10>20 &&20<10));


}



}