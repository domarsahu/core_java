// Find Cube of a number (Sqr of 4, Sqr of 7 , Sqr of 9) 

class CubeOfNo
{
	public static void main(String[] args)
	{
		
		int a = 5;
		int b = 3;
		int c = 15;
		
		int s1 = a*a*a;
		int s2 = b*b*b;
		int s3 = c*c*c;
		
		System.out.println("Cube of " +a+ " is : " +s1);
		System.out.println("Cube of " +b+ " is : " +s2);
		System.out.println("Cube of " +c+ " is : " +s3);


	}

}