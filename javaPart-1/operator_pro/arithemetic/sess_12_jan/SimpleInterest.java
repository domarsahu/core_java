/* find the simple interest where principal amount id 8 lakhs, time 60month, rate of interest is 12% */

class SimpleInterest
{
	public static void main(String[] args)
	{
		int a = 8;
		int t = 60;
		int r = 12;
		int interest = (a*t*r)/100;
		System.out.println("12 % interest  is : "+ interest );
	}
}