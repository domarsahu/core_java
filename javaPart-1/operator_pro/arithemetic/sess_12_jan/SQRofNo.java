// Find Square of a number (Sqr of 4, Sqr of 7 , Sqr of 9) 

class SQRofNo
{
	public static void main(String[] args)
	{
		
		int a = 4;
		int b = 7;
		int c = 9;
		
		int s1 = a*a;
		int s2 = b*b;
		int s3 = c*c;
		
		System.out.println("Square of 4 is : " +s1);
		System.out.println("Square of 7 is : " +s2);
		System.out.println("Square of 9 is : " +s3);


	}

}