// print Sum of all the digits of a number 

class SumAllDigits
{
	public static void main(String[] args)
	{
		int n = 78945;
		int a = 7;
		int b = 8;
		int c = 9;
		int d = 4;
		int e = 5;
		
		int sum = a+b+c+d+e;
		
		System.out.println("Given Number is : " +n);
		System.out.println("Sum of all the digis is : "+sum);
	}

}