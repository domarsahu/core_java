// WAJP to print sum of prime numbers from 1 to 20

class Prime1To20
{
	public static void main(String[] args)
	{
		int a = 1;
		int b = 2;
		int c = 3;
		int d = 5;
		int e = 7;
		int f = 13;
		int g = 17;
		int h = 19;

		int sum = a+b+c +d +e + f+g +h ;
		System.out.println(" Sumation of 1 to 20 prime number is : " + sum ); 
	}
}