// WAJP to cheak character is Lowercase alphabet or not . 

class CharLowercase
{
	public static void main(String[] args)
	{
		char var = 'f';
		int ch = var;
	
		if (ch >=97 && ch<=122)
		{
			System.out.println("Character is Lower Case");
		}else{
			System.out.println("Character is Not Lower Case");
		}
	}
}